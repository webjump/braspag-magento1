class Braspag_Core_Model_ServiceManager extends Mage_Core_Model_Abstract
{
    protected $braspagServiceClasses = [];

    /**
     * @return array
     */
    public function getServicesClasses()
    {
        $braspagServices = Mage::getConfig()->getNode('global/braspag_services')->asArray();

        array_walk_recursive($braspagServices, function($value, $key){
            if ($key == 'class') {
                $this->braspagServiceClasses[] = $value;
            }
        });

        return $this->braspagServiceClasses;
    }
}